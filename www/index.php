<H1>Welcome to the TopKLists page!</H1>
<P>For multiple full ranked (input) lists representing the same set
of N objects, the package TopKLists offers 
</P>
<OL>
	<LI><P>statistical inference on the lengths of informative (top-k)
	partial lists</P>
	<LI><P>stochastic aggregation of full or partial lists</P>
	<LI><P>graphical tools for the statistical exploration of input
	lists, and for aggregation visualization</P>
</OL>
<P>The <B>latest stable build</B> can be found on <A HREF="##" TARGET="_blank">CRAN</A>.</P>
<P>The <STRONG>project summary page</STRONG> you can find <STRONG><A HREF="https://r-forge.r-project.org/projects/topklists/" TARGET="_blank"><SPAN STYLE="text-decoration: none"><SPAN STYLE="font-weight: normal">here</SPAN></SPAN></A></STRONG>.
</P>
<P>For a <STRONG>detailed description</STRONG> of the package see the
<A HREF="##" TARGET="_blank">reference manual</A>.</P>
<P><B>Reproducible examples</B> can be found in the Vignettes
(<A HREF="##" TARGET="_blank">TopKSpace</A>, <A HREF="##" TARGET="_blank">TopKInference</A>,
<A HREF="#" TARGET="_blank">TopKGUI</A>).</P>
